        private boolean validarEntero(String nro){
        Pattern patron=Pattern.compile("^[0-9]+$");
        Matcher m=patron.matcher(nro);
        return m.matches();
        }
        private boolean validarTexto(String nro){
        Pattern patron=Pattern.compile("^[a-zA-Z\\s]+$");
        Matcher m=patron.matcher(nro);
        return m.matches();
        }
        private boolean validarCorreo(String nro){
        Pattern patron=Pattern.compile("^[\\w-\\.]+@[\\w-]+\\.[a-z]{2,4}$");
        Matcher m=patron.matcher(nro);
        return m.matches();
        }
