    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        if(pasajeroActual != null){
        pd.eliminarPasajero(pasajeroActual.getId_pasajero());
        pasajeroActual = null;
        limpiarCampos();
        } else {
        JOptionPane.showMessageDialog(this, "No hay pasajero seleccionado");
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        try{
        String dni = jtDNI.getText();
        pasajeroActual = pd.buscarPasajeroPorDNI(dni);
        
        if(pasajeroActual != null){
        
        jtNombre.setText(pasajeroActual.getNombre());
        jtApellido.setText(pasajeroActual.getApellido());
        jtCorreo.setText(pasajeroActual.getCorreo());
        jtTelefono.setText(pasajeroActual.getTelefono());
        jrEstado.setSelected(pasajeroActual.isEstado());
        }
       
       }catch(NumberFormatException ex){
       JOptionPane.showMessageDialog(this, "Debe ingresar un dni válido.");
       }
        
    }//GEN-LAST:event_jbBuscarActionPerformed
