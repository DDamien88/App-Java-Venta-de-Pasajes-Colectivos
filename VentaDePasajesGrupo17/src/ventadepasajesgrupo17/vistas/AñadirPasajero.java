    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
    String nombre = jtNombre.getText();
    String apellido = jtApellido.getText();
    String correo = jtCorreo.getText();
    String dni = jtDNI.getText();
    String telefono = jtTelefono.getText();

    if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty() || correo.isEmpty() || telefono.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No puede haber campos vacíos");
        return;
    }
    
    if (!validarEntero(dni)) {
        JOptionPane.showMessageDialog(this, "El DNI solo puede recibir numeros.");
        jtDNI.setText("");
        return;
    }
    
    if (!validarTexto(nombre)) {
        JOptionPane.showMessageDialog(this, "El nombre solo debe llevar letras");

        jtNombre.setText("");
        return;
    }
    
    if (!validarTexto(apellido)) {
        JOptionPane.showMessageDialog(this, "El apellido solo debe llevar letras");
        jtApellido.setText("");
        return;
    }
    
    if (!validarCorreo(correo)) {
        JOptionPane.showMessageDialog(this, "Ingrese el correo nuevamente, formato incorrecto:"
        +"\n" + " *Debe tener letras."
        +"\n" + " *Al menos 1 arroba."
        +"\n" + " *y 1 punto.");
        jtCorreo.setText("");
        return;
    }
    
    if (!validarEntero(telefono)) {
        JOptionPane.showMessageDialog(this,"El telefono solo puede recibir numeros.");
        jtTelefono.setText("");
        return;
    }

    Boolean estado = jrEstado.isSelected();
    if (pasajeroActual == null) {
        pasajeroActual = new Pasajero(nombre, apellido, dni, correo, telefono, true);
        pd.guardarPasajero(pasajeroActual);
    } else {
        pasajeroActual.setNombre(nombre);
        pasajeroActual.setApellido(apellido);
        pasajeroActual.setDni(dni);
        pasajeroActual.setCorreo(correo);
        pasajeroActual.setTelefono(telefono);

        pd.actualizarPasajero(pasajeroActual);
    }
    }//GEN-LAST:event_jbAgregarActionPerformed

